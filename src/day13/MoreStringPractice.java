package day13;

public class MoreStringPractice {
    public static void main(String[] args) {

        String name="Ankar";
        System.out.println(name);
        name="Ayse";
        System.out.println(name);
        name.toUpperCase();
        System.out.println(name.toUpperCase());
        name.toLowerCase();
        System.out.println(name.toLowerCase());
        name=name.toUpperCase();
        System.out.println(name);
    }
}
